function out = pncodef(coeff,reg,n)
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%% 伪随机序列（PN码）生成器
%%% coeff：输入本原多项式系数（比寄存器长度大1）
%%% reg：输入初始寄存器数值（不能全为0）
%%% n：输入预期输出的长度
%%% out：输出伪随机序列
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%-----------------调用---------------------
% coeff = [1 0 0 1 0 1];
% reg = [0 0 0 0 1];
% n = 2 * length(reg);
% out = pncodef(coeff,reg,n);
%------------------------------------------

% N=2^length(reg)-1;     %周期

for k=1:n           %计算长度n的m序列输出
    a1=mod(sum(reg.*coeff(1:length(coeff)-1)),2);   %反馈系数
    out(k)=reg(1);     %寄存器最低位输出
    reg=[reg(2:length(reg)),a1]; %寄存器位移
end
end
%也可以由primpoly（r）生成本原多项式
